-- This script sets up a database focused on water utility services.
-- It includes table creation, schema definitions, and a set of
-- INSERT statements with data modified to be water-specific.

-- Delete existing tables if they exist to start fresh
DROP TABLE IF EXISTS accounts;
DROP TABLE IF EXISTS billing_info;
DROP TABLE IF EXISTS summaries;
DROP TABLE IF EXISTS meters;
DROP TABLE IF EXISTS readings;
DROP TABLE IF EXISTS outages;
DROP TABLE IF EXISTS phone_verifications;

-- Create tables
CREATE TABLE IF NOT EXISTS accounts (
    account_id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    address TEXT,
    zip_code TEXT,
    phone TEXT,
    account_type TEXT,
    language TEXT,
    status TEXT,
    recovery_rate FLOAT,
    tax_jurisdiction_mapping_code TEXT,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS billing_info (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    account_id TEXT NOT NULL,
    current_balance FLOAT,
    unpaid_debt_recovery FLOAT,
    raw_balance FLOAT,
    days_left INTEGER,
    last_payment_date DATETIME,
    last_payment_amount FLOAT,
    FOREIGN KEY (account_id) REFERENCES accounts(account_id)
);

CREATE TABLE IF NOT EXISTS summaries (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    account_id TEXT NOT NULL,
    service_type TEXT,
    from_date DATETIME,
    to_date DATETIME,
    avg_use_amount FLOAT,
    avg_use_charge FLOAT,
    FOREIGN KEY (account_id) REFERENCES accounts(account_id)
);

CREATE TABLE IF NOT EXISTS meters (
    meter_number TEXT PRIMARY KEY,
    account_id TEXT NOT NULL,
    type_mapping_code TEXT,
    rate_mapping_code TEXT,
    service TEXT,
    multiplier FLOAT,
    tier1_rate FLOAT,
    last_disconnect DATETIME,
    FOREIGN KEY (account_id) REFERENCES accounts(account_id)
);

CREATE TABLE IF NOT EXISTS readings (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    meter_number TEXT NOT NULL,
    account_id TEXT NOT NULL,
    reading_value FLOAT,
    read_date DATETIME,
    read_from_date DATETIME,
    read_type TEXT,
    usage FLOAT,
    charge_amount FLOAT,
    tax_amount FLOAT,
    tou_peak FLOAT,
    tou_off_peak FLOAT,
    tou_shoulder FLOAT,
    FOREIGN KEY (meter_number) REFERENCES meters(meter_number),
    FOREIGN KEY (account_id) REFERENCES accounts(account_id)
);

CREATE TABLE IF NOT EXISTS outages (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    account_id TEXT NOT NULL,
    name TEXT,
    reference_number TEXT UNIQUE NOT NULL,
    address TEXT,
    nature TEXT,
    start_time DATETIME NOT NULL,
    end_time DATETIME,
    status TEXT DEFAULT 'reported',
    latitude REAL,
    longitude REAL,
    Scale TEXT,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (account_id) REFERENCES accounts(account_id)
);

CREATE TABLE IF NOT EXISTS phone_verifications (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    phone_number TEXT UNIQUE NOT NULL,
    account_id TEXT,
    verified_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    verification_method TEXT DEFAULT 'ui_verification',
    session_id TEXT,
    is_active BOOLEAN DEFAULT 1,
    FOREIGN KEY (account_id) REFERENCES accounts(account_id)
);

